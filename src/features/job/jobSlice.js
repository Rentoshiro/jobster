const jobSlice = createSlice({
  name: "job",
  initialState,
  reducers: {
    positionHandle: (state, { payload }) => {
      state[payload.name] = payload.value;
    },
  },
});

export const { positionHandle } = jobSlice.actions;
export default jobSlice.reducer;
